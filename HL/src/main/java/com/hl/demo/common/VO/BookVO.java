package com.hl.demo.common.VO;

public class BookVO {
	private int Book_Num;
	private String Book_Title;
	private String Book_Auth;
	private String Book_Pub;
	private int Book_Page;
	private String Book_Big;
	private String Book_Small;
	private String Book_ISBN;
	
	public int getBook_Num() {
		return Book_Num;
	}
	public void setBook_Num(int book_Num) {
		Book_Num = book_Num;
	}
	public String getBook_Title() {
		return Book_Title;
	}
	public void setBook_Title(String book_Title) {
		Book_Title = book_Title;
	}
	public String getBook_Auth() {
		return Book_Auth;
	}
	public void setBook_Auth(String book_Auth) {
		Book_Auth = book_Auth;
	}
	public String getBook_Pub() {
		return Book_Pub;
	}
	public void setBook_Pub(String book_Pub) {
		Book_Pub = book_Pub;
	}
	public int getBook_Page() {
		return Book_Page;
	}
	public void setBook_Page(int book_Page) {
		Book_Page = book_Page;
	}
	public String getBook_Big() {
		return Book_Big;
	}
	public void setBook_Big(String book_Big) {
		Book_Big = book_Big;
	}
	public String getBook_Small() {
		return Book_Small;
	}
	public void setBook_Small(String book_Small) {
		Book_Small = book_Small;
	}
	public String getBook_ISBN() {
		return Book_ISBN;
	}
	public void setBook_ISBN(String book_ISBN) {
		Book_ISBN = book_ISBN;
	}

}
